# 参数配置
TILE_WIDTH = 8  # 每个瓦片宽度（列数）
TILE_HEIGHT = 8  # 每个瓦片高度（行数）
NUM_TILES = 35  # 水平瓦片数量（cnt=35）
SCREEN_WIDTH = NUM_TILES * TILE_WIDTH  # 总宽度：280像素
SCREEN_HEIGHT = TILE_HEIGHT  # 总高度：8像素

# 初始化屏幕像素矩阵（0=熄灭，1=点亮）
pixels = np.zeros((SCREEN_HEIGHT, SCREEN_WIDTH), dtype=int)

x = 0
# 解析数据并填充像素矩阵
for tile_idx in range(NUM_TILES):
    # 每个瓦片占8字节，计算数据起始位置
    data_start = tile_idx * TILE_WIDTH
    # 获取8个字节
    tile_data = pos2_arr[data_start : data_start + TILE_WIDTH]

    print("下一个瓦片")
    # 按列处理瓦片数据 水平扫描
    for idx, byte in enumerate(tile_data):
        print(f"row: {idx} x: {x}")
        # 将字节转换为二进制位
        for bit in range(TILE_HEIGHT):
            val = byte & (1 << bit)
            pixels[bit, idx + x] = val

    x += 8

# 可视化点阵图（文本模式）
print("Text Visualization:")
# np.set_printoptions(threshold=np.inf, edgeitems=300, linewidth=1000000)
# print(pixels)
print("\n")
for row in pixels:
    print("".join(["1" if bit else "-" for bit in row]))
